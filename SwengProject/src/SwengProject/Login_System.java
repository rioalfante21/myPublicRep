    public void getTime(){
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");  
        LocalDateTime now = LocalDateTime.now();
        String query = "update sweng.login_table set last_log_in ='"+dtf.format(now)+"' where userid = '"+txtuserID.getText()+"'";
        try{
           Connection conn = DriverManager.getConnection(url, "sweng", "password");
           Statement stm = conn.createStatement();
           int con = stm.executeUpdate(query);
        }
        catch(SQLException ex){
            System.out.println("Error: "+ex);
        }
    }
