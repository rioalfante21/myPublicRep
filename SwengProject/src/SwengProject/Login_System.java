    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
               String user = txtuserID.getText();
               String pass = txtuserPass.getText();
            try{
                Connection conn = DriverManager.getConnection(url, "sweng", "password");
                Statement stm = conn.createStatement();
                ResultSet res = stm.executeQuery("Select * from sweng.Login_table where userid = '"+user+"' AND userpass = '"+pass+"'");
                    while(res.next()){
                    if((res.getString(1).equals(user))&& (res.getString(2).equals(pass))){
                        getTime();
                        JOptionPane.showMessageDialog(null, "Login Successfull", "Log in Success", JOptionPane.INFORMATION_MESSAGE);
                        POS_System pos = new POS_System();
                        pos.show();
                        this.hide();
                    }else{
                          JOptionPane.showMessageDialog(null, "Login Failed", "Log in Error", JOptionPane.ERROR_MESSAGE);
                          txtuserID.setText("");
                          txtuserPass.setText("");
                        }
                    }
            }catch(SQLException ex){
                System.out.println("Error: "+ ex.getMessage());
            }
               
               
    }//GEN-LAST:event_btnLoginActionPerformed

